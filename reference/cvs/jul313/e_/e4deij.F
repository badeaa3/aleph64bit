      SUBROUTINE E4DEIJ(IFLAG,IBAR,COLI,ROWJ)
C----------------------------------------------------------------------
C!  - R2 ESTIMATOR : GIVE I JK OF 3X4 STOREYS
C!
C!    AUTHOR  : D. PALLIN
C!    MODIFIED:
C!
C?
C!======================================================================
#ifndef DOC
#include "e4par0.h"
      COMMON/EIJDEU/ICOL1,ICOL2,ICOL3,ICOL4,JROW1,JROW2,JROW3,JROW4
C
C
C
C
C
C
      DUMMY=999
      ICOL1=DUMMY
      ICOL2=DUMMY
      ICOL3=DUMMY
      ICOL4=DUMMY
      JROW1=DUMMY
      JROW2=DUMMY
      JROW3=DUMMY
      JROW4=DUMMY
C
C
C
CXXXXXXXXXXXXXXXXXXXXXXXXXX
CXX ICOL1 ICOL2 JROW1 JROW2 DEFINE  TOWERS TO KEEP
CXX ICOL1 JROW1    CORRESPOND TO TOWERS WITH SMALLEST INDICES
C
      CALL E4INDI(COLI,ICOL1,ICOL2)
      CALL E4INDI(ROWJ,JROW1,JROW2)
CXX IFLAG GIVE THE COMBINATION TO CONSIDERED
CXX FLAG=0 (I1,J1)(I1,J2)(I2,J1)(I2,J2)               NORMAL
CXX      1 IDEM FALG 0 +(I3,J3)(I3,J4)(I4,J3)(I4,J4)  OVERLAP
CXX      2 (I3,J1)(I3,J2)(I4,J3)(I4,J4)               REGIONS LIMITS
CXX      3 (I3,J1)(I3,J2)(I3,J5)(I4,J3)    PATHOLOGICAL LIMIT R1-R2
C
      IFLAG=0
C
C
C
C
C
CXXXXXXXXXXX DEF TETA XXXXXXXXXX
C
CXX  TETA  BOUNDARIES
      IF(ICOL1.LT.ICOMIN)ICOL1=DUMMY
      IF(ICOL2.GT.ICOMAX)ICOL2=DUMMY
      IF(IBAR.EQ.0)THEN
      IF(COLI.GT.COOV11.AND.COLI.LE.COOV12)THEN
        ICOL2=DUMMY
        ICOL1=ICOV12
        ENDIF
      IF(COLI.GT.COOV12.AND.COLI.LE.COOV13)THEN
        ICOL2=DUMMY
        ICOL1=ICOV13
        ENDIF
      IF(COLI.GT.COOV21.AND.COLI.LE.COOV22)THEN
        ICOL2=DUMMY
        ICOL1=ICOV22
        ENDIF
      IF(COLI.GT.COOV22.AND.COLI.LE.COOV23)THEN
        ICOL2=DUMMY
        ICOL1=ICOV23
        ENDIF
      ENDIF
C   TREATEMENT IS DIFFERENT WHEN BARYCENTER
      IF(IBAR.EQ.1)THEN
CXX OVERLAP WE CHOOSE ALSO THE 4 TOWERS DEFINE BY ICOL3,4 JROW3,4
CXX OVERLAP   Z>0
      IF(COLI.GT.COOV11.AND.COLI.LE.COOV12)THEN
        ICOL2=DUMMY
        ICOL4=ICOV14
        ENDIF
      IF(COLI.GT.COOV12.AND.COLI.LE.COOV13)THEN
        ICOL1=DUMMY
        ICOL3=ICOV11
        ENDIF
      IF(ICOL1.GT.ICOV12.AND.ICOL1.LT.ICOV14) ICOL3=ICOL1-ICOVTW
      IF(ICOL1.GT.ICOV11.AND.ICOL1.LT.ICOV13) ICOL3=ICOL1+ICOVTW
      IF(ICOL2.GT.ICOV12.AND.ICOL2.LT.ICOV14) ICOL4=ICOL2-ICOVTW
      IF(ICOL2.GT.ICOV11.AND.ICOL2.LT.ICOV13) ICOL4=ICOL2+ICOVTW
CXX OVERLAP   Z<0
      IF(COLI.GT.COOV21.AND.COLI.LE.COOV22)THEN
        ICOL2=DUMMY
        ICOL4=ICOV24
        ENDIF
      IF(COLI.GT.COOV22.AND.COLI.LE.COOV23)THEN
        ICOL1=DUMMY
        ICOL3=ICOV21
        ENDIF
      IF(ICOL1.GT.ICOV22.AND.ICOL1.LT.ICOV24) ICOL3=ICOL1-ICOVTW
      IF(ICOL1.GT.ICOV21.AND.ICOL1.LT.ICOV23) ICOL3=ICOL1+ICOVTW
      IF(ICOL2.GT.ICOV22.AND.ICOL2.LT.ICOV24) ICOL4=ICOL2-ICOVTW
      IF(ICOL2.GT.ICOV21.AND.ICOL2.LT.ICOV23) ICOL4=ICOL2+ICOVTW
      ENDIF
C
C
CXXXXXXXXXXX DEF PHI  XXXXXXXXXX
C
CXX  PHI BOUNDARIES
C   BARREL +R4 EC
      IF(COLI.GE.COR412.AND.COLI.LT.COR422)THEN
         IF(JROW1.LT.JECMIN)JROW1=JBAMAX
         IF(JROW1.GT.JBAMAX)JROW1=JECMIN
         IF(JROW2.LT.JECMIN)JROW2=JBAMAX
         IF(JROW2.GT.JBAMAX)JROW2=JECMIN
         ENDIF
C   R3
      IF(   (COLI.GE.COR312.AND.COLI.LT.COR412)
     &.OR.  (COLI.GE.COR422.AND.COLI.LT.COR322))THEN
         IF(JROW1.LT.JECMIN)JROW1=JR3MAX
         IF(JROW1.GT.JR3MAX)JROW1=JECMIN
         IF(JROW2.LT.JECMIN)JROW2=JR3MAX
         IF(JROW2.GT.JR3MAX)JROW2=JECMIN
         ENDIF
C   R2
      IF(   (COLI.GE.COR212.AND.COLI.LT.COR213)
     &.OR.  (COLI.GE.COR322.AND.COLI.LT.COR222))THEN
         IF(JROW1.LT.JECMIN)JROW1=JR2MAX
         IF(JROW1.GT.JR2MAX)JROW1=JECMIN
         IF(JROW2.LT.JECMIN)JROW2=JR2MAX
         IF(JROW2.GT.JR2MAX)JROW2=JECMIN
         ENDIF
C   R1
      IF(   (COLI.LT.COR212)
     &.OR.  (COLI.GE.COR222))THEN
         IF(JROW1.LT.JECMIN)JROW1=JR1MAX
         IF(JROW1.GT.JR1MAX)JROW1=JECMIN
         IF(JROW2.LT.JECMIN)JROW2=JR1MAX
         IF(JROW2.GT.JR1MAX)JROW2=JECMIN
         ENDIF
C
CXX PHI OVERLAP
      IF(IBAR.EQ.1)THEN
      IF(COLI.GT.COOV10.AND.COLI.LE.COOV14) THEN
         JROW3=JROW1
         JROW4=JROW2
         IFLAG=1
         ENDIF
      IF(COLI.GT.COOV20.AND.COLI.LE.COOV24) THEN
         JROW3=JROW1
         JROW4=JROW2
         IFLAG=1
         ENDIF
      ENDIF
C
C
CXXXXXXXXXXXXXXXXDEF REGIONS EC XXXXXXXXXXX
CXX
CXX REGIONS LIMITS WE HAVE   JROW1 JROW2 ICOL3  => 2 TOWERS
CXX                          JROW3 JROW4 ICOL4  => 2T
CXX
CXX FOR R1-R2 REGIONS LIMIT  JROW3 ICOL4  => 1T
CXX IN CERTAIN CASES         JROW1 JROW2 JROW4 ICOL3 => 3T
C
C        R3-R4
         IF(ICOL1.EQ.(ICR411-1).OR.ICOL1.EQ.ICR421) THEN
            CALL E4R3R4(ROWJ,COLI,JROW3,JROW4
     &      ,ICOL1,ICOL2,ICOL3,ICOL4)
            IFLAG=2
            ENDIF
C        R2-R3
         IF(ICOL1.EQ.(ICR311-1).OR.ICOL1.EQ.ICR321)THEN
            CALL E4R2R3(ROWJ,COLI,JROW3,JROW4
     &      ,ICOL1,ICOL2,ICOL3,ICOL4)
            IFLAG=2
            ENDIF
C        R1-R2
         IF(ICOL1.EQ.(ICR211-1).OR.ICOL1.EQ.ICR221)
     &CALL E4R1R2(ROWJ,COLI,JROW1,JROW3,JROW4,ICOL1,ICOL2,ICOL3,
     &ICOL4,IFLAG)
C
C
C
      RETURN
      END
#endif

      SUBROUTINE BKTOAR(JW,NAME,NR,ARRAY,NDIM,NF)
      CHARACTER*4 NAME,CHAINT,TEXT*72
      COMMON/BCS/IW(1000)
      INTEGER JW(1000),ARRAY(NDIM)
      COMMON/COMCNV/LABEL,ISL,NT,NFR(128),LFR(128)
      NF=0
      IND=MLINK(JW,NAME,NR)
      IF(IND.EQ.0) GOTO 100
      DO 30 J=1,JW(IND)/18
      DO 10 I=1,18
   10 TEXT(4*I-3:4*I)=CHAINT(JW(IND+18*(J-1)+I))
      CALL CNVCHA(TEXT)
      DO 20 I=1,NT
      IF(NF.GE.NDIM) GOTO 100
      NF=NF+1
   20 ARRAY(NF)=NFR(I)
   30 CONTINUE
  100 RETURN
      END

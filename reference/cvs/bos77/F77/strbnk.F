      SUBROUTINE STRBNK

*     called in BKFRAR

      COMMON/BCS/IW(1000)
      COMMON/FMTSTR/STR
      CHARACTER*256 STR
      COMMON/STRSTR/JS,IAST,IBST,IND
      CHARACTER*4 NAME,CHAINT
      IF(JS.EQ.0.OR.IND.EQ.0) GOTO 100
      JS=0
      NAME=CHAINT(IW(IND-3))
      NR  =IW(IND-2)
      ND  =IW(IND)
      IND=NBANK(NAME,NR,ND+18)
      IF(IND.EQ.0) GOTO 100
      DO 10 I=1,18
   10 IW(IND+ND+I)=INTCHA(STR(4*I-3:4*I))
  100 RETURN
      END

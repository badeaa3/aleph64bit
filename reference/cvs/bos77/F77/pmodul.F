      SUBROUTINE PMODUL
*     PRINT TABLE OF ALL SUBNAMS, HISTOGRAM OFFSETS ETC
#include "concom.h"
      CALL TMODUL(' ')
      IF(NHN.EQ.0) GOTO 100
      WRITE(ICPR,101)
      DO 10 I=1,NHN,5
      WRITE(ICPR,102) (IHB(7,J),IHB(8,J),IHB(2,J),IHB(1,J),J=I,MIN(I+4,
     +   NHN))
   10 CONTINUE
      WRITE(ICPR,103)
  100 RETURN
  101 FORMAT('0  LIST OF MODULE NAMES'/
     +       '0',5('  SUBNAM....CALLS...IHA  '))
  102 FORMAT(1X ,5(2X,A4,A4,I7,I6,2X))
  103 FORMAT('0','  IHA = OFFSET FOR HISTOGRAMS.',
     +' HISTOGRAMMS SUPPRESSED FOR IHA = -1'/)
      END

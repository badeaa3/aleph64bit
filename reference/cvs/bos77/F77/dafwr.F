      SUBROUTINE DAFWR(IREC,ITYPE)

*     FORTRAN WRITE FOR DIRECTORY RECORD (ITYPE=0) OR DATA RECORD
*     If IREC=0 write last record, if modified
#include "dacom.h"
      COMMON/BCS/IW(1000)
#include "param.h"
      JREC=IREC
      IF(ITYPE.EQ.0) THEN
*        DIRECTORY RECORD
         IF(JREC.EQ.0.AND.IDRR.GE.0) GOTO 100
         IF(JREC.EQ.0) JREC=-IDRR/100
         IDRR=LUNDA+100*JREC
*        WRITE(UNIT=LUNDA,REC=JREC,ERR=90) (IW(IDR+I-1),I=1,NWRDA)
         CALL DAFWRS(LUNDA,JREC,IW(IDR),NWRDA,*90)
C        WRITE(6,'(''  WRITEDIR'',2I6)')  JREC
         IW(IUB+5)=IW(IUB+5)+1
      ELSE
*        DATA RECORD
         IF(JREC.EQ.0.AND.IDDR.GE.0) GOTO 100
         IF(JREC.EQ.0) JREC=-IDDR/100
         IDDR=LUNDA+100*JREC
*        WRITE(UNIT=LUNDA,REC=JREC,ERR=90) (IW(IDD+I-1),I=1,NWRDA)
         CALL DAFWRS(LUNDA,JREC,IW(IDD),NWRDA,*90)
C        WRITE(6,'(''  WRITEDAT'',2I6)')  JREC
         IW(IUB+5)=IW(IUB+5)+1
      END IF
      GOTO 100
   90 IF(IW(LUP).EQ.0) GOTO 99
      WRITE(IW(LUP),1001) LUNDA
   99 CALL BABEND('DAFWR')
  100 RETURN
 1001 FORMAT('0---------- DA-UNIT',I4,' WRITE ERROR - STOP')
      END

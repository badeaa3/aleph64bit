      SUBROUTINE EIEFFL(IFIDU)
C----------------------------------------------------------------------
C!      JOB FOR  ESTIMATORS CALCULATION
C!
C!    AUTHOR  : D. PALLIN
C!    MODIFIED:
C!
C!      CHECK QUALITY OF ESTIMATORS
C?
C!======================================================================
#ifndef DOC
#include "eiflag.h"
C%%  FIDUCIAL CUT
      IFIDU=0
      ITETU=INT(TETIFL)
      IPHU=INT(PHIIFL)
C OVERLAP REGION
      IF(ITETU.GT.45.AND.ITETU.LT.55)IFIDU=2
      IF(ITETU.GT.174.AND.ITETU.LT.184)IFIDU=2
      IF(IFIDU.NE.0)GOTO 442
C  CUT ON J
C     BARREL WE ACCEPT 3-30 MOD 12
      IF(ITETU.GT.55.AND.ITETU.LT.174)THEN
      DO 443 IK=1,12
      IF(IPHU.EQ.1+32*(IK-1))IFIDU=1
      IF(IPHU.EQ.2+32*(IK-1))IFIDU=1
      IF(IPHU.EQ.31+32*(IK-1))IFIDU=1
      IF(IPHU.EQ.32+32*(IK-1))IFIDU=1
      IF(IFIDU.NE.0)GOTO 442
 443  CONTINUE
      GOTO 442
      ENDIF
      IF(ITETU.LE.45.OR.ITETU.GE.184)THEN
C     ENDCAP R1  NEGOCIE PAR INEFFICACITE TPC
      IF(ITETU.LT.9.OR.ITETU.GT.220)IFIDU=1
      IF(IFIDU.NE.0)GOTO 442
C     ENDCAP R2  CUT J= 6 7 8 9 10  MOD 12
      IF(ITETU.LE.24.OR.ITETU.GT.205)THEN
      DO 444 IK=1,12
      IF(IPHU.EQ.6+16*(IK-1))IFIDU=1
      IF(IPHU.EQ.7+16*(IK-1))IFIDU=1
      IF(IPHU.EQ.8+16*(IK-1))IFIDU=1
      IF(IPHU.EQ.9+16*(IK-1))IFIDU=1
      IF(IPHU.EQ.10+16*(IK-1))IFIDU=1
      IF(IFIDU.EQ.1)GOTO 442
 444  CONTINUE
      GOTO 442
      ENDIF
C     ENDCAP R3  CUT J= 10 11 12 13 14 MOD 12
      IF(ITETU.LE.40.OR.ITETU.GE.189)THEN
      DO 445 IK=1,12
      IF(IPHU.EQ.10+24*(IK-1))IFIDU=1
      IF(IPHU.EQ.11+24*(IK-1))IFIDU=1
      IF(IPHU.EQ.12+24*(IK-1))IFIDU=1
      IF(IPHU.EQ.13+24*(IK-1))IFIDU=1
      IF(IPHU.EQ.14+24*(IK-1))IFIDU=1
      IF(IFIDU.EQ.1)GOTO 442
 445  CONTINUE
      GOTO 442
      ENDIF
C     ENDCAP R4  CUT J= 14 15 16 17 18 MOD 12
      IF(ITETU.LE.50.OR.ITETU.GE.179)THEN
      DO 446 IK=1,12
      IF(IPHU.EQ.14+32*(IK-1))IFIDU=1
      IF(IPHU.EQ.15+32*(IK-1))IFIDU=1
      IF(IPHU.EQ.16+32*(IK-1))IFIDU=1
      IF(IPHU.EQ.17+32*(IK-1))IFIDU=1
      IF(IPHU.EQ.18+32*(IK-1))IFIDU=1
      IF(IFIDU.EQ.1)GOTO 442
 446  CONTINUE
      ENDIF
      ENDIF
 442  CONTINUE
      RETURN
      END
#endif

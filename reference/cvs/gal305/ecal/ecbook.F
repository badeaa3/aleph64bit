*DK ecbook
      SUBROUTINE ECBOOK
C.-----------------------------------------------------------------
C
C! Book ECAL histograms
C.-----------------------------------------------------------------
#ifndef DOC
      SAVE
C
#include "jobcom.h"
#include "jqcom.h"
#include "ecstat.h"
C
C    booking of histograms
C
      IF(TKINJO.EQ.'LUND'.OR.TKINJO.EQ.'USER')  THEN
           XMAX=BKINJO(4)*500.
       ELSE IF(TKINJO.EQ.'PART') THEN
           XMAX = BKINJO(4)*1000.
       ENDIF
       EMAX=0.05
       IDSKI=439
       NB1SKI=50
       NB2SKI=100
       SKIZ=0.
       ZLMAX1=5.
       ZLMAX2=20.
       NPSKI=45
       ZPSKI=45.
       CALL HBOOK2(IDSKI,'TRACK ELTS PHOTONS : LI VS EI (CM,KEV)$',
     &NB1SKI,SKIZ,EMAX,NB1SKI,SKIZ,ZLMAX2,SKIZ)
       CALL HBOOK2(IDSKI+1,'TRACK ELTS (E+,E-) : LI VS EI (CM,KEV)$',
     &NB1SKI,SKIZ,EMAX,NB1SKI,SKIZ,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+2,'ANALOG SIGNAL BARREL S1 + S2 (MEV)$',NB2SKI,
     &ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+3,'EC ANALOG SIGNAL BARREL S3 (MEV)$',NB2SKI,
     &ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+4,'ANALOG SIGNAL ENDCAP S1 + S2 (MEV)$',NB2SKI,
     &ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+5,'ANALOG SIGNAL ENDCAP S3 (MEV)$',NB2SKI,
     &ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK2(IDSKI+6,'ODD VS EVEN PLANES (ANALOG.) (MEV)$',
     &NB1SKI,SKIZ,XMAX,NB1SKI,SKIZ,XMAX,SKIZ)
       CALL HBOOK1(IDSKI+7,'TOTAL ANALOG SIGNAL ON THE WIRES (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+8,'LONG. PROFILE OF WIRES (ANALOG.) (MEV)$',
     &NPSKI,SKIZ,ZPSKI,SKIZ)
       CALL HBOOK1(IDSKI+9,'NB. OF HITS/WIRE PLANES (ANALOG.)$',
     &NPSKI,SKIZ,ZPSKI,SKIZ)
       CALL HBOOK1(IDSKI+10,'ANALOG SIGNAL IN TOWERS ALL STACKS (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+11,'ANALOG SIGNAL IN TOWERS STACK1 (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+12,'ANALOG SIGNAL IN TOWERS STACK2 (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+13,'ANALOG SIGNAL IN TOWERS STACK3 (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK2(IDSKI+16,'ODD VS EVEN PLANES (DIGIT.) (MEV)$',
     &NB1SKI,SKIZ,XMAX,NB1SKI,SKIZ,XMAX,SKIZ)
       CALL HBOOK1(IDSKI+17,'TOTAL DIGITAL SIGNAL ON THE WIRES (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+18,'LONGIT. PROFILE OF WIRES (DIGIT.) (MEV)$',
     &NPSKI,SKIZ,ZPSKI,SKIZ)
       CALL HBOOK1(IDSKI+19,'NUMBER OF HITS/WIRE PLANES (DIGIT)$',
     &NPSKI,SKIZ,ZPSKI,SKIZ)
       CALL HBOOK1(IDSKI+20,'DIGIT. SIGNAL IN TOWERS ALL STACKS (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+21,'DIGIT. SIGNAL IN TOWERS STACK1 (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+22,'DIGIT. SIGNAL IN TOWERS STACK2 (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+23,'DIGIT. SIGNAL IN TOWERS STACK3 (MEV)$',
     &NB2SKI,ZLMAX1,ZLMAX1,SKIZ)
       CALL HBOOK1(IDSKI+24,'EC DIGIT. NB OF HIT TOWERS/EVT$',NB2SKI,
     &ZLMAX1,ZLMAX1,SKIZ)
       CALL HIDOPT(IDSKI+2,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+3,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+4,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+5,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+7,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+9,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+10,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+11,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+12,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+13,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+17,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+19,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+20,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+21,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+22,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+23,'1EVL','AEVL')
       CALL HIDOPT(IDSKI+24,'1EVL','AEVL')
      END
#endif
